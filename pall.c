#include "monty.h"
#include <stdlib.h>
#include <stdio.h>

#define LIMIT 100
/**
 * pall - pushes all elements into the stack
 * Return: 0 Success
 */
void pall()
{
	int i, top, stack[LIMIT];

	for (i == top; i >= 0; i--)
	{
		printf("%d\n", stack[i]);
	}
	if (stack[LIMIT] == NULL)
	{
		return;
	}
}
